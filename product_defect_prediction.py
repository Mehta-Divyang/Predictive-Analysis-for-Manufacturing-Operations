import pandas as pd
import numpy as np

dataframe = pd.read_csv('Manufacturing Dataset.csv')

dataframe

dataframe = dataframe[['Product ID', 'Process temperature [K]', 'Operational Hours', 'Failure Type']]
dataframe

dataframe.isnull().sum(axis=0)

dataframe = dataframe.rename(columns={'Product ID':'Machine_ID','Process temperature [K]':'Temperature','Operational Hours':'Run_Time','Failure Type':'Downtime_Flag'})

dataframe['Downtime_Flag'] = np.where((dataframe['Downtime_Flag']=='No Failure'),0,1)

dataframe

dataframe.to_csv("machine_maintenance_data.csv",index=False)